  const NEWS_LANGUAGE_KEY = "aaia_news_language_v1";
  const SUPPORTED_LANGUAGES = ["en", "zh"];
  const languageListeners = new Set();

  function toText(value) {
    return typeof value === "string" ? value.trim() : "";
  }

  function toLanguageCode(value) {
    if (typeof value !== "string") return "en";
    const normalized = value.trim().toLowerCase();
    return SUPPORTED_LANGUAGES.includes(normalized) ? normalized : "en";
  }

  function readStoredLanguage() {
    try {
      const saved = window.localStorage.getItem(NEWS_LANGUAGE_KEY);
      return toLanguageCode(saved);
    } catch (error) {
      return "en";
    }
  }

  let currentLanguage = readStoredLanguage();

  function isNewsPageContext() {
    const body = document.body;
    if (!body) return false;
    return body.classList.contains("news-page") || body.classList.contains("news-detail-page");
  }

  function applyDocumentLanguageState(languageCode) {
    if (!isNewsPageContext()) return;
    const html = document.documentElement;
    if (html) {
      html.lang = languageCode === "zh" ? "zh-Hant" : "en";
    }
    if (document.body) {
      document.body.setAttribute("data-news-language", languageCode);
    }
  }

  function applyLanguageStateToSwitches(root) {
    if (!root || typeof root.querySelectorAll !== "function") return;
    root.querySelectorAll("[data-news-lang-btn]").forEach((button) => {
      const target = toLanguageCode(button.getAttribute("data-news-lang-btn"));
      const isActive = target === currentLanguage;
      button.classList.toggle("active", isActive);
      button.setAttribute("aria-pressed", isActive ? "true" : "false");
    });
  }

